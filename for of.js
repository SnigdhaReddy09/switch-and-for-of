// 1
var day="monday"
switch (day) {
    case "sunday":
        console.log("1st day of week")
        break;
    case "monday":
        console.log("2nd day of week")
        break;
    case "tuesday":
        console.log("3rd day of week")
        break;
    case "wednesday":
        console.log("4th day of week")
        break;
    case "thursday":
        console.log("5th day of week")
        break;
    case "friday":
        console.log("6th day of week")
        break;
    case "saturday":
        console.log("7th day of week")
        break;
    default:
        console.log("invalid")    
}

